    /**
     * 以图搜图 (360)
     */
    @PostMapping("/search/picture/so")
    public BaseResponse<List<SoImageSearchResult>> searchPictureByPictureIsSo(@RequestBody SearchPictureByPictureRequest searchPictureByPictureRequest) {
        // 参数校验保持不变
        ThrowUtils.throwIf(searchPictureByPictureRequest == null, ErrorCode.PARAMS_ERROR);
        Long pictureId = searchPictureByPictureRequest.getPictureId();
        ThrowUtils.throwIf(pictureId == null || pictureId <= 0, ErrorCode.PARAMS_ERROR);
        Picture picture = pictureService.getById(pictureId);
        ThrowUtils.throwIf(picture == null, ErrorCode.NOT_FOUND_ERROR);

        List<SoImageSearchResult> results = new ArrayList<>();
        String imageUrl = StrUtil.isNotBlank(picture.getUrl()) ? picture.getUrl() : picture.getThumbnailUrl();
        int page = 0; // 假设start为页码，每页固定20条
        final int maxPages = 3; // 最大尝试页数，防止无限循环

        while (results.size() < 50 && page < maxPages) {
            List<SoImageSearchResult> temp = SoImageSearchApiFacade.searchImage(imageUrl, page);
            if (temp.isEmpty()) break;

            // 计算剩余所需数量并截断
            int remaining = 50 - results.size();
            if (temp.size() > remaining) {
                results.addAll(temp.subList(0, remaining));
                break;
            } else {
                results.addAll(temp);
            }
            page++; // 正确递增页码
        }
        return ResultUtils.success(results);
    }

